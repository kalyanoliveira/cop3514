#include <stdio.h>

int main(void)
{
    int n = 10;

    switch (n)
    {
        case 2: printf("2\n");
        case 9: printf("9\n");
        case 3: printf("3\n");
        case 8: printf("8\n");
        case 5: printf("5\n");
        case 6: printf("6\n");
        case 1: printf("1\n");
        case 10: printf("10\n");
        case 4: printf("4\n");
        case 7: printf("7\n");
    }

    return 0;
}
